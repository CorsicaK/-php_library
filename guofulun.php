			<p class="cap_title">作者信息</p>
			<p class="cap_desc">作者：亚当·斯密<br>作者简介：亚当·斯密（1723年6月5日—1790年7月17日），出生在苏格兰法夫郡（County Fife）的寇克卡迪（Kirkcaldy），英国经济学家、哲学家、作家，经济学的主要创立者。亚当·斯密是现代资本主义经济制度的创立者，强调自由市场、自由贸易以及劳动分工，被誉为“经济学之父”。1723年他出生于苏格兰一个海关官员的家庭，14岁考入格拉斯哥大学，学习数学和哲学，并对经济学产生兴趣。17岁时转入牛津学院。毕业后，1748年到爱丁堡大学讲授修辞学与文学。1751～1764年回格拉斯哥大学执教，期间他的伦理学讲义经修订在1759年以《道德情操论》为名出版，为他赢得了声誉。1764年他辞了教授。担任私人教师，并到欧洲旅行，结识了伏尔泰等名流，对他有很大影响。1767年他辞职，回家乡写作《国富论》，9年后《国富论》出版。1787年他出任格拉斯哥大学校长。1790年逝世于苏格兰爱丁堡 。</p><br>
			<p class="cap_title">图书简介</p>
			<p class="cap_desc">《国富论》全称为《国民财富的性质和原因的研究》，是英国古典经济学家亚当·斯密用了近十年时间创作的经济学著作，首次出版于1776年。《国富论》认为人的本性是利己的，追求个人利益是人民从事经济活动的唯一动力。同时人又是理性的，作为理性的经济人，人们能在个人的经济活动中获得最大的个人利益。如果这种经济活动不会受到干预，那么，经由价格机制这只“看不见的手”引导，人们不仅会实现个人利益的最大化，还会推进公共利益。《国富论》这部著作奠定了资本主义自由经济的理论基础，该书的出版标志着古典政治经济学理论体系的建立，堪称西方经济学界的“圣经”。</p><br>
			<p class="cap_title">精彩书评</p>
			<p class="cap_desc">美国政治经济学家熊彼特：《国富论》不仅是最为成功的经济学著作，而且或许是除了达尔文的《物种起源》外迄今出版的最为成功的科学著作。<br> 英国历史学家巴克勒：从最终效果来看，《国富论》也许是迄今最重要的书，这本书对人类幸福做出的贡献，超过了所有名垂青史的政治家和立法者作的贡献的总和。<br> 英国经济史学家特伦斯·哈奇森：斯密的《国富论》标志着经济思想史上的一个新纪元或者说一场革命。</p><br>

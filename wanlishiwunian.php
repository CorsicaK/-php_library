			<p class="cap_title">图书简介</p>
			<p class="cap_desc">《万历十五年》全书共分七章，分别写了最高统治者万历皇帝、大学士申时行、首辅张居正、模范官僚海瑞、自由派知识分子李贽、抗倭英雄戚继光等,同时，也写了他们之间存在的重重矛盾，如皇帝与群臣的矛盾，保守派与自由派的矛盾，官员与官员之间的矛盾等等。无论是皇帝还是官僚，是将军还是知识分子，都各有自己的理想，又都各有自己的局限，最终在相互制衡冲突中彼此消耗，导致了整个国家的全面平庸与危机，整个中国社会逐渐走向衰亡的趋向似乎不可避免。<br>此书写作历时七年，作者先后翻阅过大量史料,在明史研究方面也有非常扎实的功底，因此虽然是横断面，但是非常严谨。另一方面，作者又用“梦幻般”的笔触，既敏感又客观冷静地在一定程度上还原了“历史”，使读者仿佛能够触摸到历史真实的脉搏，感受到一个朝代的呼吸。</p><br>
			<p class="cap_title">精彩书评</p>
			<p class="cap_desc">作者在全文中，以深厚的史学素养和远见卓识，用大胆、平实的笔触评说明史，进而评述中国的历史。他在这本史著中赋予了历史真实鲜活的面容，不仅可以使人们清晰地体验到光明与黑暗、进步与倒退的较量，更重要的是给人们以遐想的空间和回味的余地。细心的读者不仅可以从中品味个人的人生酸楚，而且还会发现隐藏于表面背后的历史积淀。用这种方式撰写历史，不仅能唤起人们对往昔客观历史的记忆，更重要的是让人们从深厚的历史迷雾中走出，进而思索。因此，读过此书的人都说:“读《万历十五年》给人现实意义上的启迪远在其史料价值之上。”</p><br>
